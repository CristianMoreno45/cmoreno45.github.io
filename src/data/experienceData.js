const GetStack = (items) => {
  let stackList = [];
  let outputArray = [];
  let others = [
    "visual basic .Net",
    "genexus evo3",
    ".Net Core",
    "sonar cloud",
    "datadog",
    "scrum",
    "kanban",
  ];

  let count = 0;
  let start = false;
  items.forEach((exp) => {
    exp.StackItem.forEach((el) => {
      stackList.push(el);
    });
  });

  for (const element of stackList) {
    for (const item of outputArray) {
      if (element === item) {
        start = true;
      }
    }
    count++;
    if (count === 1 && start === false) {
      outputArray.push(element);
    }
    start = false;
    count = 0;
  }

  return outputArray.concat(others);
};
